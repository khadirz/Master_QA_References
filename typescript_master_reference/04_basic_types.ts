/*
04 - Basic Types
TypeScript lets us define what type of value a variable can hold.
*/
let firstName: string = "Khadir"; // string means text
let year: number = 2026; // number means integer or decimal
let price: number = 19.99; // also number
let isLoggedIn: boolean = false; // boolean means true or false
let emptyValue: null = null; // null means intentionally empty
let notAssigned: undefined = undefined; // undefined means not assigned
console.log(firstName, year, price, isLoggedIn, emptyValue, notAssigned);

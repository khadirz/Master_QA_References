/*
06 - Numbers and Math Operators
Basic math in TypeScript.
*/
const a = 10;
const b = 3;
console.log(a + b); // Addition
console.log(a - b); // Subtraction
console.log(a * b); // Multiplication
console.log(a / b); // Division
console.log(a % b); // Remainder
console.log(a ** b); // Power
const total = 5 + 3 * 2; // Multiplication first
console.log(total);
const totalWithParentheses = (5 + 3) * 2; // Parentheses first
console.log(totalWithParentheses);

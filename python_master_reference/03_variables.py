"""
03 - Variables
A variable stores a value.
"""

name = "Khadir"  # String variable

age = 40  # Integer variable

price = 19.99  # Float variable

is_tester = True  # Boolean variable

print(name)  # Print the value of name

print(age)  # Print the value of age

print(price)  # Print the value of price

print(is_tester)  # Print True or False

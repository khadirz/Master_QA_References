"""
04 - Basic Data Types
Common Python data types.
"""

text_value = "Hello"  # str means string/text

integer_value = 100  # int means whole number

float_value = 10.5  # float means decimal number

boolean_value = False  # bool means True or False

nothing_value = None  # None means no value

print(type(text_value))  # type() shows the data type

print(type(integer_value))

print(type(float_value))

print(type(boolean_value))

print(type(nothing_value))

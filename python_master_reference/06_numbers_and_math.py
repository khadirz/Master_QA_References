"""
06 - Numbers and Math Operators
Basic math in Python.
"""

a = 10

b = 3

print(a + b)  # Addition

print(a - b)  # Subtraction

print(a * b)  # Multiplication

print(a / b)  # Division

print(a // b)  # Floor division

print(a % b)  # Remainder

print(a ** b)  # Power

total = 5 + 3 * 2  # Multiplication first

print(total)

total_with_parentheses = (5 + 3) * 2  # Parentheses first

print(total_with_parentheses)
